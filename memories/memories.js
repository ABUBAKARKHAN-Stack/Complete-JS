// MEMORIES IN JS //
// TWO TYPES
            //  1. Stack Memory
            //  2. Heap Memory
                                        // STACK MEMORY //
//  Stack memory used in primitive Datatypes   
// In case of STACK MEMORY You will get a copy of a Variable that you've decleared //   
//  EXAMPLE
    //  let myFbName = "Abubakar Khan"
    //  let anotherName = myFbName
    //  anotherName = "Abubakar Tanoli"
    //  console.log(myFbName);
    //  console.log(anotherName);
    // OUTPUT => Abubakar Khan ,  Abubakar Tanoli  Because These decleared variable is in Stack Memory  //


